export function getListPrompt(city: string, url: string): string {
  return `
Мне нужна твоя помощь в извлечении информации о достопримечательностях из города ${city}.

**Цель:** Загрузи и проанализируй содержимое веб-страницы по URL \`${url}\` и создай JSON-массив с данными о туристических объектах.

**Требуемый формат:**
Пожалуйста, верни результат в виде \`array json\`. Каждый объект в массиве должен иметь следующую структуру:
\`\`\`json
[{
  "location_name": "string",
  "rating": "number",
  "category": "string",
  "url": "string",
  "imageUrl": ["string"],
  "reviewSnippet": "string"
}]
\`\`\`

**Инструкции по заполнению полей:**
*   \`location_name\`: Название достопримечательности (например, "Hongya Cave").
*   \`rating\`: Рейтинг, основанный на отзывах (например, 4.0).
*   \`category\`: Категория объекта (например, "Caverns & Caves").
*   \`imageUrl\`: Массив URL-адресов изображений объекта.
*   \`reviewSnippet\`: Краткий фрагмент отзыва.

*   \`url\`: **КРИТИЧЕСКИ ВАЖНО - АБСОЛЮТНАЯ ТОЧНОСТЬ!**
    *   Ты должен извлечь относительный путь к странице достопримечательности.
    *   Затем ты ОБЯЗАН соединить его с базовым доменом \`https://www.tripadvisor.com\`, чтобы получить полную, абсолютную ссылку.
    *   **Не придумывай, не угадывай и не сокращай URL.** Он должен быть точной копией того, что используется на сайте.
`
}
